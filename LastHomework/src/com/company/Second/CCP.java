//The Common closure principle
package com.company.Second;

public class CCP {
    private String Name;
    private int Year;


    public String getName() {
        return Name;
    }

    public void setName(String n) {
        this.Name = n;
    }

    public int getYear() {
        return Year;
    }

    public void setYear(int y) {
        this.Year = y;
    }
}




